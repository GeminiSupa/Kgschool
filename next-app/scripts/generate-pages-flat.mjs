/**
 * Generates src/i18n/pagesFlat.generated.ts from route title tables.
 * Run: node scripts/generate-pages-flat.mjs
 */
import fs from 'fs'
import path from 'path'
import { fileURLToPath } from 'url'
import { T } from './routeTitlesData.mjs'

const __dirname = path.dirname(fileURLToPath(import.meta.url))


function buildFlat(which) {
  const out = {}
  for (const [route, o] of Object.entries(T)) {
    out[`${route}.title`] = o[which]
  }
  return out
}

const header = `// AUTO-GENERATED by scripts/generate-pages-flat.mjs — do not edit by hand
import type { AppLanguage } from './constants'

export const pagesFlat: Record<AppLanguage, Record<string, string>> = {
  de: ${JSON.stringify(buildFlat('de'), null, 2)},
  en: ${JSON.stringify(buildFlat('en'), null, 2)},
  tr: ${JSON.stringify(buildFlat('tr'), null, 2)},
}
`

const outPath = path.join(__dirname, '../src/i18n/pagesFlat.generated.ts')
fs.writeFileSync(outPath, header)
console.log('Wrote', outPath, 'keys:', Object.keys(T).length * 3, 'title strings')
